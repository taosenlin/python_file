# 一、字符集概述
# 字符集就是一套文字符号及其编码的描述


# 二、重要概念

# 位： 计算机的最小单位  二进制中的一位  用二进制的0/1表示

# 字节：八位组成一个字节

# 字符：我们肉眼可见的文字与符号

# 字符集：字符的集合

# 编码：将字符转换成计算机可识别的0/1代码

# 解码：将计算机表示的0/1编码转换成肉眼可见的字符



# 三、字符编码现状：大一统

# utf8  可变长

# 内存里(底层)为 unicode         (读出来或者在互联网转载)计算机转载为 utf8

