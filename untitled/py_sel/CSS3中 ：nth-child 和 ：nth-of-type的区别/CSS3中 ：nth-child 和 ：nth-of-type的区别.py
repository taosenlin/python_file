# 1、 :nth-child()  选择器
#  :nth-child(n) 选择器匹配属于其父元素的第N个子元素，不论元素类型,
#  n 可以是数字、关键词或公式.


# 2、 :nth-of-type(n)  选择器
#  :nth-of-type(n) 选择器匹配属于父元素的特定类型的第N个子元素的每个元素,
#  n 可以是数字、关键词或公式.



# p:nth-child(6)  选择 p 元素所在的父元素下的第6个子元素,且该元素是 p 元素(不是就报错)
# 即: <p>第5个p</p>

# p:nth-of-type(7)  选择 p 元素所在的父元素下的第7个 p 元素
# 即: <p>第7个p</p>
















































































